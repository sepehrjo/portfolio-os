function RootShell({ children }: { children: ReactNode }) {
  return (
    <html lang="en">
      <head>
        <HeadContent />
        {/* Pre-hydration script: read preferred-language cookie and set html lang/dir
            This runs before the page paints to reduce flash of un-localized content. */}
        <script
          // inline on purpose
          dangerouslySetInnerHTML={{
            __html: `(() => {
  try {
    var m = document.cookie.match(new RegExp('(?:^|; )' + 'preferred-language' + '=([^;]*)'));
    var lang = m ? decodeURIComponent(m[1]) : 'en';
    if (lang) {
      document.documentElement.lang = lang;
      document.documentElement.dir = (lang === 'fa') ? 'rtl' : 'ltr';
    }
  } catch (e) { /* ignore */ }
})();`,
          }}
        />
      </head>
      <body>
        <a
          href="#main-content"
          style={{
            position: "absolute",
            left: "-9999px",
            top: "auto",
            width: "1px",
            height: "1px",
            overflow: "hidden",
          }}
          onFocus={(e) => {
            e.currentTarget.style.left = "6px";
            e.currentTarget.style.top = "6px";
            e.currentTarget.style.width = "auto";
            e.currentTarget.style.height = "auto";
            e.currentTarget.style.overflow = "visible";
          }}
          onBlur={(e) => {
            e.currentTarget.style.left = "-9999px";
          }}
        >
          Skip to content
        </a>
        {children}
        <Scripts />
      </body>
    </html>
  );
}

function RootComponent() {
  const { queryClient } = Route.useRouteContext();

  return (
    <I18nProvider defaultLanguage="en">
      <QueryClientProvider client={queryClient}>
        {/* Required: nested routes render here. Removing <Outlet /> breaks all child routes. */}
        <Outlet />
      </QueryClientProvider>
    </I18nProvider>
  );
}
